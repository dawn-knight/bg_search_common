package com.mbgo.search.autokey;

public class KeywordBean {

	private String word;
	private int sort;
	private int nSort;
	
	public int getnSort() {
		return nSort;
	}

	public void setnSort(int nSort) {
		this.nSort = nSort;
	}

	public KeywordBean() {
	}
	
	public KeywordBean(String word, int sort) {
		super();
		this.word = word;
		this.sort = sort;
	}
	
	public KeywordBean(String word, int sort, int nsort) {
		super();
		this.word = word;
		this.sort = sort;
		this.nSort = nsort;
	}

	public String getWord() {
		return word;
	}
	public void setWord(String word) {
		this.word = word;
	}
	public int getSort() {
		return sort;
	}
	public void setSort(int sort) {
		this.sort = sort;
	}
}
